package com.smetrix.app.model;

/**
 * Перечисление статусов позиции сметы (EstimateItem).
 *
 * Отражает текущее физическое/логистическое состояние материала или работы
 * в рамках проекта. Хранится в базе данных как строка (String), а не как
 * числовой код, чтобы избежать хрупкости при рефакторинге.
 *
 * Жизненный цикл статуса:
 *  - Позиция добавлена в смету → NEED_TO_BUY
 *  - Материал заказан у поставщика → ORDERED
 *  - Материал доставлен на объект → ON_SITE
 *  - Единица измерения в ФГИС ЦС не совпала с единицей в смете → UNITS_MISMATCH
 */
public enum ItemStatus {

    /**
     * Материал/работа добавлены в смету, но закупка ещё не начата.
     * Это начальный статус при создании позиции сметы.
     */
    NEED_TO_BUY,

    /**
     * Заявка на закупку оформлена, материал заказан у поставщика.
     * Пользователь вручную переводит позицию в этот статус.
     */
    ORDERED,

    /**
     * Материал физически доставлен и принят на строительном объекте.
     * Пользователь вручную подтверждает факт доставки.
     */
    ON_SITE,

    /**
     * Обнаружено несоответствие единицы измерения в ФГИС ЦС и в локальной смете.
     * Требует ручной проверки и исправления пользователем.
     * Устанавливается автоматически при синхронизации с данными ФГИС.
     */
    UNITS_MISMATCH
}
