package com.smetrix.app.db.entity;

import androidx.annotation.NonNull;
import androidx.room.ColumnInfo;
import androidx.room.Entity;
import androidx.room.PrimaryKey;

/**
 * Room Entity для таблицы "conflict".
 *
 * Хранит информацию о конфликтах синхронизации, которые не удалось
 * разрешить автоматически и требуют ручного вмешательства пользователя.
 *
 * Когда возникает конфликт:
 *  Конфликт происходит, когда одна и та же запись (например, ProjectEntity)
 *  была изменена одновременно на клиенте (в offline-режиме) и на сервере
 *  другим пользователем или устройством. При попытке синхронизации сервер
 *  возвращает HTTP 409 Conflict, и SyncWorker фиксирует этот конфликт
 *  в данной таблице вместо автоматического перезаписывания данных.
 *
 * Стратегия разрешения:
 *  Приложение показывает пользователю экран разрешения конфликтов,
 *  где он может выбрать: оставить локальную версию, принять серверную,
 *  или просмотреть обе и принять решение по полям. После разрешения
 *  запись из таблицы conflict удаляется (ConflictDao.delete()).
 *
 * Первичный ключ — entityId:
 *  Один конфликт на одну сущность. Если та же запись конфликтует снова
 *  (что маловероятно, но возможно), старый конфликт перезаписывается.
 *  entityId — это id конфликтующей записи (например, id проекта или комнаты).
 *
 * Поля localSnapshot и serverSnapshot:
 *  Хранят полные JSON-снимки конфликтующих версий записи.
 *  Это позволяет UI показать пользователю все поля обеих версий для сравнения.
 *  JSON сериализуется/десериализуется в Repository-слое через Gson.
 *  Пример localSnapshot:
 *   {"id":"uuid-123","name":"Кухня (изменено)","length":"3.5","syncState":"CONFLICT"}
 *
 * Поле entityType:
 *  Строка-метка, указывающая тип конфликтующей сущности.
 *  Используется UI, чтобы понять, какой экран разрешения показать пользователю.
 *  Допустимые значения: "PROJECT", "PROJECT_ROOM", "ESTIMATE_ITEM",
 *  "WORK_TASK", "WORKER".
 */
@Entity(
    tableName = "conflict"
)
public class ConflictEntity {

    /**
     * Идентификатор конфликтующей сущности.
     * Это НЕ id самого конфликта, а id той записи (проекта, комнаты и т.д.),
     * по которой возник конфликт. Например, если конфликтует ProjectEntity
     * с id "proj-uuid-456", то entityId = "proj-uuid-456".
     *
     * Ограничение: один entityId — один активный конфликт в таблице.
     * Если тот же объект конфликтует повторно, запись перезаписывается.
     */
    @PrimaryKey
    @NonNull
    @ColumnInfo(name = "entity_id")
    public String entityId;

    /**
     * Тип конфликтующей сущности.
     * Строка-метка для идентификации таблицы/типа объекта.
     * Допустимые значения:
     *  - "PROJECT"        — конфликт в таблице project
     *  - "PROJECT_ROOM"   — конфликт в таблице project_room
     *  - "ESTIMATE_ITEM"  — конфликт в таблице estimate_item
     *  - "WORK_TASK"      — конфликт в таблице work_task
     *  - "WORKER"         — конфликт в таблице worker
     * Используется в ConflictResolutionActivity для выбора правильного
     * UI-экрана и десериализации JSON-снимков в нужный класс.
     */
    @ColumnInfo(name = "entity_type")
    public String entityType;

    /**
     * JSON-снимок локальной (клиентской) версии конфликтующей записи.
     * Содержит все поля записи на момент обнаружения конфликта (до синхронизации).
     * Сериализуется через Gson в SyncWorker перед сохранением в БД.
     * Отображается пользователю как "Моя версия" на экране разрешения конфликтов.
     * Пример: "{\"id\":\"uuid\",\"name\":\"Кухня мой вариант\",\"version\":3}"
     */
    @ColumnInfo(name = "local_snapshot")
    public String localSnapshot;

    /**
     * JSON-снимок серверной версии конфликтующей записи.
     * Содержит все поля записи, возвращённые сервером в ответе HTTP 409.
     * Сериализуется через Gson в SyncWorker перед сохранением в БД.
     * Отображается пользователю как "Версия сервера" на экране разрешения.
     * Пример: "{\"id\":\"uuid\",\"name\":\"Кухня правка коллеги\",\"version\":5}"
     */
    @ColumnInfo(name = "server_snapshot")
    public String serverSnapshot;

    /**
     * Временная метка обнаружения конфликта.
     * UNIX-timestamp в миллисекундах. Устанавливается в SyncWorker
     * в момент получения ответа HTTP 409 от сервера.
     * Используется для сортировки конфликтов в UI (новые — сверху)
     * и для потенциальной автоматической очистки старых конфликтов.
     */
    @ColumnInfo(name = "detected_at")
    public long detectedAt;

    /**
     * Пустой конструктор, обязательный для Room.
     * Room использует его при десериализации строк из базы данных в объекты Java.
     */
    public ConflictEntity() {
        // Пустой конструктор требуется Room для десериализации строк БД.
    }
}
