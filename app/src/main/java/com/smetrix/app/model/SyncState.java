package com.smetrix.app.model;

/**
 * Перечисление состояний синхронизации для каждой записи в локальной базе данных.
 *
 * Каждая Entity-запись хранит строковое представление этого enum в поле syncState.
 * Мы намеренно храним его как String (а не ordinal/int), чтобы добавление новых
 * значений enum в будущем не ломало существующую базу данных.
 *
 * Жизненный цикл состояний:
 *  - Запись создаётся локально → PENDING_CREATE
 *  - Запись изменяется локально → PENDING_UPDATE
 *  - Запись помечается к удалению → PENDING_DELETE
 *  - SyncWorker успешно отправил изменение на сервер → SYNCED
 *  - SyncWorker получил сетевую ошибку → FAILED
 *  - Сервер вернул конфликт версий → CONFLICT
 */
public enum SyncState {

    /**
     * Запись полностью синхронизирована с сервером.
     * Локальная версия совпадает с серверной версией (поле version).
     */
    SYNCED,

    /**
     * Запись была создана локально и ещё не отправлена на сервер.
     * SyncWorker должен выполнить HTTP POST для этой записи.
     */
    PENDING_CREATE,

    /**
     * Запись была изменена локально и ещё не отправлена на сервер.
     * SyncWorker должен выполнить HTTP PUT/PATCH для этой записи.
     */
    PENDING_UPDATE,

    /**
     * Запись помечена для удаления (soft delete).
     * SyncWorker должен выполнить HTTP DELETE для этой записи.
     * После подтверждения сервером запись физически удаляется из Room.
     */
    PENDING_DELETE,

    /**
     * Последняя попытка синхронизации завершилась ошибкой (сеть недоступна,
     * сервер вернул 5xx и т.д.). SyncWorker попробует повторить позднее.
     */
    FAILED,

    /**
     * Обнаружен конфликт: локальная версия и серверная версия расходятся.
     * Требуется ручное разрешение пользователем через ConflictResolutionActivity.
     * Соответствующая запись сохраняется в таблице conflict.
     */
    CONFLICT
}
