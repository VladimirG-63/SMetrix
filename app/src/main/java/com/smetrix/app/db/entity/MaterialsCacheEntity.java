package com.smetrix.app.db.entity;

import androidx.annotation.NonNull;
import androidx.room.ColumnInfo;
import androidx.room.Entity;
import androidx.room.Index;

import java.math.BigDecimal;

/**
 * Room Entity для таблицы "materials_cache".
 *
 * Представляет запись о материале/товаре, закешированную из реестра ФГИС ЦС
 * (Федеральная государственная информационная система ценообразования в строительстве)
 * или добавленную пользователем вручную.
 *
 * Назначение кэша:
 *  Приложение работает в offline-first режиме. Чтобы пользователь мог добавлять
 *  позиции сметы без интернета, мы кэшируем данные о материалах локально.
 *  Поиск по кэшу выполняется быстро через SQL LIKE по полям name и fgis_code.
 *
 * Первичный ключ — fgisCode:
 *  Код ФГИС ЦС уникален для каждого материала/работы в реестре, поэтому он
 *  идеально подходит как первичный ключ. При обновлении данных с сервера
 *  запись с тем же fgisCode просто перезаписывается (INSERT OR REPLACE).
 *  Уникальность также явно задана через @Index(unique = true) для ясности.
 *
 * Поле source:
 *  Указывает происхождение записи:
 *  - "SERVER" — данные получены с сервера (синхронизированы с ФГИС ЦС).
 *  - "MANUAL" — запись добавлена пользователем вручную (нет кода ФГИС).
 *    Для записей типа MANUAL в качестве fgisCode используется UUID.
 *
 * Поле priorityScore:
 *  Целочисленный рейтинг для сортировки результатов поиска.
 *  Материалы с более высоким баллом показываются выше в списке.
 *  Сервер вычисляет этот балл на основе частоты использования.
 *
 * Поле regionCode:
 *  Код региона (например, "77" для Москвы, "78" для СПб).
 *  Цены в ФГИС ЦС зависят от региона, поэтому одна запись = одна цена
 *  в одном регионе. Индексируется для фильтрации по региону.
 *
 * Таблица не имеет sync-полей (version, syncState):
 *  Кэш материалов управляется только сервером. Клиент не инициирует
 *  изменения этих данных — он только читает с сервера и кэширует локально.
 *  Поэтому стандартные sync-поля не нужны.
 */
@Entity(
    tableName = "materials_cache",
    primaryKeys = {"fgis_code", "region_code"},
    indices = {
        @Index(value = {"region_code"}),
        @Index(value = {"name"})
    }
)
public class MaterialsCacheEntity {

    /**
     * Код материала/работы в реестре ФГИС ЦС.
     * Уникальный идентификатор записи в таблице.
     *
     * Для записей из реестра ФГИС ЦС — официальный код, например "ФСС-2022-001".
     * Для записей, добавленных вручную пользователем (source = "MANUAL") —
     * генерируется UUID, чтобы гарантировать уникальность первичного ключа.
     */
    @NonNull
    @ColumnInfo(name = "fgis_code")
    public String fgisCode;

    /**
     * Название материала или вида работы.
     * Пример: "Краска интерьерная акриловая белая" или "Стяжка цементная".
     * Индексируется для быстрого текстового поиска: WHERE name LIKE ?
     */
    @ColumnInfo(name = "name")
    public String name;

    /**
     * Единица измерения из реестра ФГИС ЦС.
     * Пример: "м2", "м3", "кг", "шт", "пм".
     * Может не совпадать с единицей в приложении (см. UnitConversionEntity).
     */
    @ColumnInfo(name = "unit_measure")
    public String unitMeasure;

    /**
     * Код региона, для которого актуальна данная цена.
     * Формат: двузначный код субъекта РФ по ОКАТО/ОКТМО.
     * Пример: "77" (Москва), "78" (Санкт-Петербург), "66" (Свердловская обл.).
     * Индексируется для быстрой фильтрации: WHERE region_code = ?
     */
    @NonNull
    @ColumnInfo(name = "region_code")
    public String regionCode;

    /**
     * Базовая цена за единицу измерения по данным ФГИС ЦС.
     * Хранится как TEXT через BigDecimalConverter для финансовой точности.
     * Пример: 450.75 (рублей за квадратный метр).
     */
    @ColumnInfo(name = "base_price")
    public BigDecimal basePrice;

    @ColumnInfo(name = "consumption_rate")
    public BigDecimal consumptionRate;

    /**
     * Источник происхождения записи.
     * Допустимые значения (строки):
     *  - "SERVER" — данные синхронизированы с сервера (реестр ФГИС ЦС).
     *  - "MANUAL" — запись добавлена пользователем вручную.
     */
    @ColumnInfo(name = "source")
    public String source;

    /**
     * Временная метка кэширования записи.
     * UNIX-timestamp в миллисекундах. Показывает, когда данные были
     * получены с сервера или добавлены вручную. Используется для
     * определения "свежести" кэша и принятия решения об обновлении.
     */
    @ColumnInfo(name = "cached_at")
    public long cachedAt;

    /**
     * Рейтинг приоритета для сортировки результатов поиска.
     * Целое число: чем выше значение, тем выше позиция в поисковой выдаче.
     * Сервер вычисляет балл на основе частоты использования материала
     * среди всех пользователей. Материалы с баллом 0 отображаются последними.
     */
    @ColumnInfo(name = "priority_score")
    public int priorityScore;

    /**
     * Пустой конструктор, обязательный для Room.
     * Room использует его при десериализации строк из базы данных в объекты Java.
     */
    public MaterialsCacheEntity() {
        // Пустой конструктор требуется Room для десериализации строк БД.
    }
}
