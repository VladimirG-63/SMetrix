package com.smetrix.app.model;

import java.util.UUID;

/**
 * Утилитарный класс для генерации уникальных идентификаторов (UUID).
 *
 * <p>В текущей реализации используется стандартный {@link UUID#randomUUID()},
 * который генерирует UUID версии 4 (случайный). Этот подход является
 * временным placeholder-ом и достаточен для разработки и тестирования.
 *
 * <p><b>Почему UUID v7 в будущем?</b><br>
 * UUID v7 (RFC 9562) — монотонно возрастающий идентификатор на основе
 * Unix-времени. Он лучше подходит для первичных ключей баз данных,
 * так как:
 * <ol>
 *   <li>Обеспечивает хронологическую сортировку записей без дополнительных индексов.</li>
 *   <li>Снижает фрагментацию B-tree индекса в SQLite (вставки всегда в конец).</li>
 *   <li>Позволяет определить время создания записи по её идентификатору.</li>
 * </ol>
 *
 * <p>Этот класс намеренно сделан non-instantiable (конструктор приватный),
 * так как содержит только статические утилитарные методы.
 */
public final class UuidGenerator {

    /**
     * Приватный конструктор запрещает создание экземпляров этого класса.
     * Все методы — статические.
     */
    private UuidGenerator() {
        // Запрет создания экземпляров утилитарного класса
        throw new UnsupportedOperationException("UuidGenerator — утилитарный класс, создавать экземпляры запрещено.");
    }

    /**
     * Генерирует новый уникальный идентификатор в формате UUID.
     *
     * <p>Использует стандартный {@link UUID#randomUUID()} (UUID v4, 122 бита энтропии).
     * Достаточен для всех текущих нужд приложения.
     *
     * @return строка UUID в формате {@code xxxxxxxx-xxxx-xxxx-xxxx-xxxxxxxxxxxx}.
     *         Никогда не {@code null}.
     */
    public static String generate() {
        return UUID.randomUUID().toString();
    }
}
